/**
 * @author Tom
 *
 */
public class Triangle {

	/**
	 * @param args
	 */
	public float a;
	public float b;
	public float c;
	
	public Triangle (float sa, float sb, float sc){
		this.a = sa;
		this.b = sb;
		this.c = sc;
	}
	public float getA() {
		return this.a;
	}
	public void setA(float sa){
		this.a = sa;
	}
	public float getB(){
		return this.b;
	}
	public void setB(float sb){
		this.b = sb;
	}
	public float getC() {
		return this.c;
	}
	public void setC(float sc){
		this.c = sc;
	}
	
	

}
